use std::collections::HashMap;

use regex::Regex;

use crate::UnicodeMapping;

fn parse_mapping(s: &str) -> HashMap<String, usize> {
    let re = Regex::new(r" (\S+)\s+([0-9A-Za-f]{4}).*").unwrap();
    let mut res = HashMap::new();
    let m = re.captures_iter(s);
    for c in m {
        res.insert(
            c.get(1).unwrap().as_str().to_string(),
            u32::from_str_radix(c.get(2).unwrap().as_str(), 16).unwrap() as usize,
        );
    }
    res
}

