fn parse(s: &str) -> UnicodeMapping {
    let mapping = parse_mapping(EBCDIC_SYMBOL_MAP);
    let mut res = [None; 256];
    for (i, v) in s.split_whitespace().enumerate() {
        res[i] = mapping.get(v).map(|v| char::from_u32(*v as u32).unwrap());
    }
    res
}

pub fn parse_ibm037() -> UnicodeMapping {
    parse(IBM037)
}

pub fn parse_ibm1140() -> UnicodeMapping {
    parse(IBM1140)
}
